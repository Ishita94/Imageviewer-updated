   public ImageIcon croppedimage(File f,int ix,int iy,int fx,int fy,ImageIcon cimage) throws IOException
   {
       
        BufferedImage buffImg = new BufferedImage(cimage.getIconWidth(),cimage.getIconHeight(), BufferedImage.TYPE_INT_ARGB);
        buffImg=ImageIO.read(f);
        BufferedImage buffImg2 = buffImg.getSubimage(ix,iy,fx,fy); //fill in the corners of the desired crop location here
        BufferedImage buffImg3 = new BufferedImage(buffImg2.getWidth(), buffImg2.getHeight(), BufferedImage.TYPE_INT_RGB);
        Graphics g = buffImg3.createGraphics();
         g.drawImage(buffImg2, 0, 0, null);
         g.dispose();
        Image pimage=Toolkit.getDefaultToolkit().createImage(buffImg3.getSource());
        ImageIcon m=new ImageIcon(pimage);
        return m;
   }
