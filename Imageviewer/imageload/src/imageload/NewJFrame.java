    public  ImageIcon resize(File c,int scaledWidth, int scaledHeight)
            throws IOException {
        // reads input image
        //File inputFile = new File(inputImagePath);
        BufferedImage inputImage = ImageIO.read(c);
 
        // creates output image
        outputImage = new BufferedImage(scaledWidth,
                scaledHeight, inputImage.getType());
 
        // scales the input image to the output image
        Graphics2D g2d = outputImage.createGraphics();
        g2d.drawImage(inputImage, 0, 0, scaledWidth, scaledHeight, null);
        g2d.dispose();
        Image pimage=Toolkit.getDefaultToolkit().createImage(outputImage.getSource());
        ImageIcon m=new ImageIcon(pimage);
        //System.out.println(m.getDescription());
        //jLabel1.setIcon(m);
       return m;
       
    }
    
    
